/******************************************************************
 *
 * aw8624 i2c read/write
 *
 ****************************************************************/
static int aw8624_i2c_write(uint8_t addr, uint8_t val)
{
    int ret = 0;
    uint8_t data_buf[] = { addr, val };

    /* Create a i2c_msg buffer, that is used to put the controller into write
       mode and then to write some data. */
    struct i2c_msg msg_buf[] = { {AW8624_I2C_ADDRESS,
                      I2C_M_WR, 2, data_buf}
    };

    ret = qup_i2c_xfer(aw8624_i2c_dev, msg_buf, 1);
    if(ret < 0) {
        dprintf(CRITICAL, "qup_i2c_xfer write error %d\n", ret);
        return ret;
    }
    return 0;
}

static int aw8624_i2c_read(uint8_t addr, uint8_t *val)
{
    int ret = 0;
    uint8_t data_buf[] = { addr, val };

    /* Create a i2c_msg buffer, that is used to put the controller into write
       mode and then to write some data. */
    struct i2c_msg msg_buf[] = {
            {AW8624_I2C_ADDRESS, I2C_M_WR, 1, &addr},
            {AW8624_I2C_ADDRESS, I2C_M_RD, 1, val},
    };

    ret = qup_i2c_xfer(aw8624_i2c_dev, msg_buf, 2);
    if(ret < 0) {
        dprintf(CRITICAL, "qup_i2c_xfer read error %d\n", ret);
        return ret;
    }
    return 0;
}

static int aw8624_i2c_write_bits(uint8_t addr, uint8_t mask, uint8_t value)
{
    uint8_t reg_val;

    aw8624_i2c_read(addr, &reg_val);
    reg_val &= mask;
    reg_val |= value;
    aw8624_i2c_write(addr, reg_val);
}

/******************************************************************
 *
 * aw8624 haptic
 *
 ****************************************************************/
static int aw8624_hw_init(void)
{
    struct pm8x61_gpio gpio;
    uint8_t reg_val;
    int ret;

    dprintf(CRITICAL, "aw8624_init enter\n");

    gpio.direction = PM_GPIO_DIR_IN;
    gpio.function = 0;
    gpio.pull = PM_GPIO_PULL_UP_30;
    gpio.vin_sel = 2;
    pm8x41_gpio_config(2, &gpio);

    aw8624_i2c_dev = qup_blsp_i2c_init(BLSP_ID_2, QUP_ID_0, 100000, 19200000);
    if(!aw8624_i2c_dev) {
        dprintf(CRITICAL, "qup_blsp_i2c_init failed\n");
    }

    ret = aw8624_i2c_read(0x00, &reg_val);
    if(ret) {
        dprintf(CRITICAL, "aw8624 i2c read failed\n");
        ret = -2;
        return ret;
    }

    if(reg_val == AW8624_CHIP_ID) {
        dprintf(CRITICAL, "aw8624 read id ok\n");
        ret = 0;
    } else {
        dprintf(CRITICAL, "aw8624 read id err\n");
        ret = -1;
    }

    return ret;
}

static int aw8624_deinit(void)
{
    dprintf(CRITICAL, "aw8624_deint enter\n");
    clock_disable_blsp_i2c(BLSP_ID_2, QUP_ID_0);
    qup_i2c_deinit(aw8624_i2c_dev);

    return 0;
}

static int aw8624_haptic_init(void)
{
    int ret = 0;

    aw8624_i2c_write_bits(AW8624_REG_SYSCTRL,
        AW8624_BIT_SYSCTRL_WORK_MODE_MASK, AW8624_BIT_SYSCTRL_STANDBY);


    return ret;
}

static int aw8624_ram_init(void)
{
    int ret = 0;
    int i = 0;

    /* RAMINIT Enable */
    aw8624_i2c_write_bits(AW8624_REG_SYSCTRL,
        AW8624_BIT_SYSCTRL_RAMINIT_MASK, AW8624_BIT_SYSCTRL_RAMINIT_EN);

    /* RAM Base Addr */
    aw8624_i2c_write(AW8624_REG_BASE_ADDRH, AW8624_RAM_BASE_ADDRH);
    aw8624_i2c_write(AW8624_REG_BASE_ADDRL, AW8624_RAM_BASE_ADDRL);

    /* ram */
    aw8624_i2c_write(AW8624_REG_RAMADDRH, AW8624_RAM_BASE_ADDRH);
    aw8624_i2c_write(AW8624_REG_RAMADDRL, AW8624_RAM_BASE_ADDRL);
    for(i=0; i<aw8624_ram_len; i++) {
        aw8624_i2c_write(AW8624_REG_RAMDATA, aw8624_ram_data[i]);
    }

    /* RAMINIT Disable */
    aw8624_i2c_write_bits(AW8624_REG_SYSCTRL,
        AW8624_BIT_SYSCTRL_RAMINIT_MASK, AW8624_BIT_SYSCTRL_RAMINIT_OFF);

    return ret;
}

static int aw8624_haptic_play(void)
{
    int ret = 0;
    kal_uint8 wav_index = 0x01;
    kal_uint8 wav_loop = 0x00;
    kal_uint8 bst_vol = 0x22;
    kal_uint8 gain = 0x80;

    /* Seq 1 */
    aw8624_i2c_write(AW8624_REG_WAVSEQ1, wav_index);
    aw8624_i2c_write(AW8624_REG_WAVSEQ2, 0x00);

    /* Loop 1 */
    aw8624_i2c_write(AW8624_REG_WAVLOOP1, wav_loop);

    /* Bst Vol */
    aw8624_i2c_write_bits(AW8624_REG_BSTCFG,
        AW8624_BIT_BSTCFG_BSTVOL_MASK, bst_vol);

    /* Gain */
    aw8624_i2c_write(AW8624_REG_DATDBG, gain);

    /* Play Mode */
    aw8624_i2c_write_bits(AW8624_REG_SYSCTRL,
        AW8624_BIT_SYSCTRL_PLAY_MODE_MASK, AW8624_BIT_SYSCTRL_PLAY_MODE_RAM);

    /* Haptic Play */
    aw8624_i2c_write_bits(AW8624_REG_SYSCTRL,
        AW8624_BIT_SYSCTRL_WORK_MODE_MASK, AW8624_BIT_SYSCTRL_ACTIVE);
    aw8624_i2c_write_bits(AW8624_REG_GO, AW8624_BIT_GO_MASK,
        AW8624_BIT_GO_ENABLE);

    return ret;
}

int aw8624_haptic(void)
{
    int ret = -1;

    ret = aw8624_hw_init();
    if(ret) {
        dprintf(CRITICAL, "%s: hw init err %d \n", __func__, ret);
        return ret;
    }

    ret = aw8624_haptic_init();
    if(ret) {
        dprintf(CRITICAL, "%s: haptic init err %d \n", __func__, ret);
        return ret;
    }

    ret = aw8624_ram_init();
    if(ret) {
        dprintf(CRITICAL, "%s: ram init err %d \n", __func__, ret);
        return ret;
    }

    ret = aw8624_haptic_play();
    if(ret) {
        dprintf(CRITICAL, "%s: haptic play err %d \n", __func__, ret);
        return ret;
    }

    return ret;
}


